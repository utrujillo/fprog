# Matriz de 2x4
matriz = [
  [1, 5, 10, 20],
  [1, 5],
  [4, 2, 90, 5]
]

# Longitud de la matriz
print( len(matriz) )

# Longitud de la fila
print( len(matriz[1]) )

# Imprimiendo toda la matriz
print( matriz )

# Imprimiento toda la fila
print( matriz[1] )

# Imprimiendo un valor especifico
print( matriz[0][2] )